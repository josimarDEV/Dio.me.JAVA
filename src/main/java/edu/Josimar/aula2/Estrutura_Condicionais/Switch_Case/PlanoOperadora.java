package edu.Josimar.aula2.Estrutura_Condicionais.Switch_Case;

public class PlanoOperadora {
    public static void main(String[] args) {
        String plano = "M";

        switch (plano) {
            case "T": {
                System.out.println("5GB Youtube");
            }
            case "M": {
                System.out.println("whats + Instagram grátis");
            }
            case "B": {
                System.out.println("100 minutos ligação");
            }
        }

    }
}
